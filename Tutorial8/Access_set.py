#Acessing items in Sets
    #You cannot access items in a set by referring to an index or a key.But you can loop through the set items using a for loop, or ask if a specified 
    # value is present in a set, by using the in keyword.

my_set = {"apple", "banana", "cherry"}

for x in my_set:
  print(x)


#Once a set is created, you cannot change its items, but you can add new items.

