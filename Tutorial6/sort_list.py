#Sort()

#Alphabetically
thislist = ["orange", "mango", "kiwi", "pineapple", "banana"]
thislist.sort()
print(thislist)

#Numerically
my_list = [2,1,9,10,45]
my_list.sort()
print(my_list)

#Descending order
li = [100, 67, 12, 56, 90]
li.sort()
print(li)

#Case insensitive sort
my_list = ["banana", "Orange", "Kiwi", "cherry"]
my_list.sort()
print(my_list)

#Reversing order
my_list = ["banana", "orange", "kiwi", "cherry", "mango"]
my_list.reverse()
print(my_list)

