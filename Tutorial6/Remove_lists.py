#Remove() - The remove() method removes the specified item.(specified item)
my_list = ["apple", "banana", "cherry"]
my_list.remove("banana")
print(my_list)

#Removing specified index - pop() function is used to remove specified index
my_list = ["apple", "banana", "cherry"]
my_list.pop(2)
print(my_list)

#clear the list
my_list = ["apple", "banana", "cherry"]
my_list.clear()
print(my_list)