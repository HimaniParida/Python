#Loop through a list
my_list =["apple", "banana", "mango", "cherry"]
for x in my_list:
    print(x)


#Loop through the index numbers
my_list = ["apple", "cherry", "mango", "banana", "blackcurrant"]
for i in range(len(my_list)):
    print(my_list[i])

#Using a while loop
thislist = ["apple", "banana", "cherry"]
i = 0
while i < len(thislist):
  print(thislist[i])
  i = i + 1