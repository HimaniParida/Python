#change item value
li = ["apple", "banana", "cherry"]
li[1] = "mango"
print(li)

#changing the range of item value
li = ["apple", "banana", "cherry", "kiwi", "watermelon", "litchi", "apple","dates"]
li[1:3] = ["blackcurrant", "orange"]
print(li)


#insert items
li = ["apple", "banana", "cherry", "kiwi", "watermelon", "litchi", "apple","dates"]
li.insert(2, "blackcurrant")
print(li)
