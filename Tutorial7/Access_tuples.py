#Access tuple items
my_tuple = ("Blackcurrant", "Mango", "Cherry","Apple", "Banana")
print(my_tuple[3])

#Negative Indexing
my_tuple = ("Blackcurrant", "Mango", "Cherry","Apple", "Banana")
print(my_tuple[-4])

#Range_of_Indexes
my_tuple = ("Blackcurrant", "Mango", "Cherry","Apple", "Banana")
print(my_tuple[2 : 5])

#Indexing from the beginning
my_tuple = ("apple", "banana", "cherry", "orange", "kiwi", "melon", "mango")
print(my_tuple[:4])


#Indexing from the end 
my_tuple = ("apple", "banana", "cherry", "orange", "kiwi", "melon", "mango")
print(my_tuple[2:])